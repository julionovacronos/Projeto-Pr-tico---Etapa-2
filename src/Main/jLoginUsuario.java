    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nome = jNome.getText();
        String senha = jSenha.getText();
        System.out.println(senha);
        UsuarioController dao = new UsuarioController();
        List<Usuario> l = dao.listar();
        for (Iterator it = l.iterator(); it.hasNext();){
            Usuario user = (Usuario) it.next();
            System.out.println(user.getNome()+""+user.getSenha());
            if(user.getLogin().equals(nome)&&user.getSenha().equals(senha)){
                JOptionPane.showMessageDialog(rootPane, "Bem Vindo "+user.getNome());
                Menu m = new Menu();
                m.setVisible(true);
                m.setUser(user);
                this.dispose();
                return;
            }
        }
        JOptionPane.showMessageDialog(rootPane, "Usuario e senha nao cadastrados");
    }//GEN-LAST:event_jButton1ActionPerformed

